int test_rsa_pkey_keygen(void *data)
{
    int err;
    EVP_PKEY_CTX *ctx = NULL;
    EVP_PKEY *pkey = NULL;
#if OPENSSL_VERSION_NUMBER >= 0x30000000L
    const RSA *rsaKey = NULL;
#else
    RSA *rsaKey = NULL;
#endif
    BIGNUM *eCmd = NULL;
    BIGNUM *n = NULL;
    BIGNUM *eKey = NULL;
#if defined(HAVE_FIPS) || defined(HAVE_FIPS_VERSION)
    /* Generating a 3072-bit key is slow, so only do it if we have to because
     * we're using wolfCrypt FIPS. Can't do 2048 because that's the default. */
    const int newKeySize = 3072;
    const int badKeyGenSizes[] = {512, 1024};
#else
    const int newKeySize = 1024;
    const int badKeyGenSizes[] = {256};
#endif /* HAVE_FIPS || HAVE_FIPS_VERSION */
    const int numBad = sizeof(badKeyGenSizes) / sizeof(*badKeyGenSizes);
    int i = 0;

    (void)data;
    (void)rsa_key_der_256;
    (void)rsa_key_der_1024;

    err = (ctx = EVP_PKEY_CTX_new_from_name(wpLibCtx, "RSA", NULL)) == NULL;
    if (err == 0) {
        err = EVP_PKEY_keygen_init(ctx) != 1;
    }
    if (err == 0) {
        PRINT_MSG("Change the key size w/ ctrl command");
        err = EVP_PKEY_CTX_ctrl(ctx, EVP_PKEY_RSA, EVP_PKEY_OP_KEYGEN,
                                EVP_PKEY_CTRL_RSA_KEYGEN_BITS, newKeySize,
                                NULL) <= 0;
    }
    if (err == 0) {
        err = (eCmd = BN_new()) == NULL;
    }
    if (err == 0) {
        err = BN_set_word(eCmd, 3) != 1;
    }
    if (err == 0) {
         PRINT_MSG("Change the public exponent w/ ctrl command");
        err = EVP_PKEY_CTX_ctrl(ctx, EVP_PKEY_RSA, EVP_PKEY_OP_KEYGEN,
                                EVP_PKEY_CTRL_RSA_KEYGEN_PUBEXP, 0, eCmd) <= 0;
    }
    if (err == 0) {
        PRINT_MSG("Generate RSA key w/ new parameters");
        err = EVP_PKEY_keygen(ctx, &pkey) != 1;
    }
    if (err == 0) {
        rsaKey = EVP_PKEY_get0_RSA(pkey);
        err = rsaKey == NULL;
    }
    if (err == 0) {
        PRINT_MSG("Verify new parameters were used");
        RSA_get0_key(rsaKey, (const BIGNUM **)&n, (const BIGNUM **)&eKey, NULL);
        err = BN_num_bits(n) != newKeySize;
    }
    if (err == 0) {
        err = BN_cmp(eCmd, eKey) != 0;
    }

    if ((err == 0) && (!noKeyLimits)) {
        PRINT_MSG("Check that ctrl commands to set invalid key gen size fail");
        for (; i < numBad && err != 1; ++i) {
            err = EVP_PKEY_CTX_ctrl(ctx, EVP_PKEY_RSA, EVP_PKEY_OP_KEYGEN,
                                    EVP_PKEY_CTRL_RSA_KEYGEN_BITS,
                                    badKeyGenSizes[i], NULL) == 1;
        }
    }

    BN_free(eCmd);
    EVP_PKEY_free(pkey);
    EVP_PKEY_CTX_free(ctx);

    return err;
}

int test_rsa_get_params(void *data)
{
    int err;
    EVP_PKEY *pkey = NULL;
    unsigned char n[2048 / 8];
    unsigned char e[2048 / 8];
    OSSL_PARAM params[3];
    EVP_PKEY_CTX *ctx = NULL;
    BIGNUM *eCmd = NULL;
    BIGNUM *eRet = NULL;
    const int newKeySize = 2048;
    (void)data;

    err = (ctx = EVP_PKEY_CTX_new_from_name(wpLibCtx, "RSA", NULL)) == NULL;
    if (err == 0) {
        err = EVP_PKEY_keygen_init(ctx) != 1;
    }
    if (err == 0) {
        PRINT_MSG("Change the key size w/ ctrl command");
        err = EVP_PKEY_CTX_ctrl(ctx, EVP_PKEY_RSA, EVP_PKEY_OP_KEYGEN,
                                EVP_PKEY_CTRL_RSA_KEYGEN_BITS, newKeySize,
                                NULL) <= 0;
    }
    if (err == 0) {
        err = (eCmd = BN_new()) == NULL;
    }
    if (err == 0) {
        err = BN_set_word(eCmd, 3) != 1;
    }
    if (err == 0) {
        PRINT_MSG("Change the public exponent w/ ctrl command");
        err = EVP_PKEY_CTX_ctrl(ctx, EVP_PKEY_RSA, EVP_PKEY_OP_KEYGEN,
                                EVP_PKEY_CTRL_RSA_KEYGEN_PUBEXP, 0, eCmd) <= 0;
    }
    if (err == 0) {
        PRINT_MSG("Generate RSA key w/ new parameters");
        err = EVP_PKEY_keygen(ctx, &pkey) != 1;
    }
    if (pkey == NULL) {
        err = 1;
    }
    if (err == 0) {
        memset(e, 0, sizeof(e));
        memset(n, 0, sizeof(n));
        params[0] = OSSL_PARAM_construct_BN(OSSL_PKEY_PARAM_RSA_N, n, sizeof(n));
        params[1] = OSSL_PARAM_construct_BN(OSSL_PKEY_PARAM_RSA_E, e, sizeof(e));
        params[2] = OSSL_PARAM_construct_end();

        PRINT_MSG("Getting RSA params");

        if (EVP_PKEY_get_params(pkey, params) != 1) {
            err = 1;
        }
    }
    /* Check return sizes, then verify e matches the one we set */
    if (err == 0) {
        if ((params[0].return_size != (size_t)(newKeySize / 8)) ||
            (params[1].return_size != 1)) {
            err = 1;
        }
    }
    if (err == 0) {
        eRet = BN_bin2bn(e, (int)params[1].return_size, NULL);
        if (eRet == NULL) {
            err = 1;
        }
    }
    if (err == 0) {
        err = BN_cmp((const BIGNUM *)eCmd, (const BIGNUM *)eRet);
    }

    BN_free(eCmd);
    BN_free(eRet);
    EVP_PKEY_free(pkey);
    EVP_PKEY_CTX_free(ctx);
    return err;
}

