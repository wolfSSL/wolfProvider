static int test_rsa_enc_dec(const unsigned char *der, size_t derLen,
                            int padMode, const EVP_MD *rsaMd,
                            const EVP_MD *rsaMgf1Md)
{
    int err;
    int res;
    EVP_PKEY *pkey = NULL;
#if OPENSSL_VERSION_NUMBER >= 0x30000000L
    const RSA *rsaKey = NULL;
#else
    RSA *rsaKey = NULL;
#endif
    unsigned char *rsaEnc = NULL;
    size_t rsaEncLen = 0;
    size_t bufLen = 20;
    unsigned char *buf = NULL;

    PRINT_MSG("Load RSA key");
    pkey = d2i_PrivateKey(EVP_PKEY_RSA, NULL, &der, derLen);
    err = pkey == NULL;
    if (err == 0) {
        rsaKey = EVP_PKEY_get0_RSA(pkey);
        err = rsaKey == NULL;
    }
    if (err == 0) {
        rsaEncLen = RSA_size(rsaKey);
        rsaEnc = (unsigned char*)OPENSSL_zalloc(rsaEncLen);
        err = rsaEnc == NULL;
    }
    if (err == 0) {
        if (padMode == RSA_NO_PADDING) {
            bufLen = rsaEncLen;
        }
        buf = (unsigned char *)OPENSSL_zalloc(bufLen);
        err = buf == NULL;
    }
    if (err == 0) {
        err = RAND_bytes(buf, (int)bufLen) == 0;
    }
     if (err == 0 && padMode == RSA_NO_PADDING) {
        /* Set the MSB to 0 so there's no chance the number is too large for the
         * RSA modulus. */
        buf[0] = 0;
    }

    if (err == 0) {
        PRINT_MSG("Test encrypt/decrypt arbitrary data");
        PRINT_MSG("Encrypt with OpenSSL");
        err = test_pkey_enc(pkey, osslLibCtx, buf, bufLen, rsaEnc, rsaEncLen,
                            padMode, rsaMd, rsaMgf1Md);
    }
    if (err == 0) {
        PRINT_MSG("Decrypt with wolfprovider");
        err = test_pkey_dec(pkey, wpLibCtx, buf, bufLen, rsaEnc, rsaEncLen,
                            padMode, rsaMd, rsaMgf1Md);
    }
    if (err == 0) {
        PRINT_MSG("Decrypt bad cipher text with wolfprovider");
        rsaEnc[1] ^= 0x80;
        res = test_pkey_dec(pkey, wpLibCtx, buf, bufLen, rsaEnc, rsaEncLen,
                            padMode, rsaMd, rsaMgf1Md);
        if (res != 1)
            err = 1;
    }
    if (err == 0) {
        PRINT_MSG("Encrypt with wolfprovider");
        rsaEncLen = RSA_size(rsaKey);
        err = test_pkey_enc(pkey, wpLibCtx, buf, bufLen, rsaEnc, rsaEncLen,
                            padMode, rsaMd, rsaMgf1Md);
    }
    if (err == 0) {
        PRINT_MSG("Decrypt with OpenSSL");
        err = test_pkey_dec(pkey, osslLibCtx, buf, bufLen, rsaEnc, rsaEncLen,
                            padMode, rsaMd, rsaMgf1Md);
    }

    EVP_PKEY_free(pkey);

    if (rsaEnc)
        OPENSSL_free(rsaEnc);
    if (buf)
        OPENSSL_free(buf);

    return err;
}

