static int test_rsa_sign_verify_pad(int padMode, const EVP_MD *md,
    const EVP_MD *mgf1Md)
{
    int err;
    int res;
    EVP_PKEY *pkey = NULL;
#if OPENSSL_VERSION_NUMBER >= 0x30000000L
    const RSA *rsaKey = NULL;
#else
    RSA *rsaKey = NULL;
#endif
    unsigned char *rsaSig = NULL;
    size_t rsaSigLen = 0;
    size_t bufLen = 20;
    unsigned char *buf = NULL;
    const unsigned char *p = rsa_key_der_2048;

    PRINT_MSG("Load RSA key");
    pkey = d2i_PrivateKey(EVP_PKEY_RSA, NULL, &p, sizeof(rsa_key_der_2048));
    err = pkey == NULL;
    if (err == 0) {
        rsaKey = EVP_PKEY_get0_RSA(pkey);
        err = rsaKey == NULL;
    }
    if (err == 0) {
        rsaSigLen = RSA_size(rsaKey);
        rsaSig = (unsigned char*)OPENSSL_malloc(rsaSigLen);
        err = rsaSig == NULL;
    }
    if (err == 0) {
        if (padMode == RSA_NO_PADDING) {
            bufLen = rsaSigLen;
        }
        else if (padMode == RSA_X931_PADDING) {
            bufLen = EVP_MD_size(md);
        }
        buf = (unsigned char *)OPENSSL_malloc(bufLen);
        err = buf == NULL;
    }
    if (err == 0) {
        err = RAND_bytes(buf, (int)bufLen) == 0;
    }
     if (err == 0 && padMode == RSA_NO_PADDING) {
        /* Set the MSB to 0 so there's no chance the number is too large for the
         * RSA modulus. */
        buf[0] = 0;
    }

    /* Don't run these first tests in the case of PSS, which is strictly for
     * signatures and not arbitrary data. */
    if ((err == 0) && (padMode != RSA_PKCS1_PSS_PADDING)) {
        PRINT_MSG("Test signing/verifying arbitrary data");
        PRINT_MSG("Sign with OpenSSL");
        err = test_pkey_sign(pkey, osslLibCtx, buf, bufLen, rsaSig, &rsaSigLen,
            padMode, md, mgf1Md);
    }
    if ((err == 0) && (padMode != RSA_PKCS1_PSS_PADDING)) {
        PRINT_MSG("Verify with wolfprovider");
        err = test_pkey_verify(pkey, wpLibCtx, buf, bufLen, rsaSig, rsaSigLen,
            padMode, md, mgf1Md);
    }
    if ((err == 0) && (padMode != RSA_PKCS1_PSS_PADDING)) {
        PRINT_MSG("Verify bad signature with wolfprovider");
        rsaSig[1] ^= 0x80;
        res = test_pkey_verify(pkey, wpLibCtx, buf, bufLen, rsaSig, rsaSigLen,
            padMode, md, mgf1Md);
        if (res != 1)
            err = 1;
    }
    if ((err == 0) && (padMode != RSA_PKCS1_PSS_PADDING)) {
        PRINT_MSG("Sign with wolfprovider");
        rsaSigLen = RSA_size(rsaKey);
        err = test_pkey_sign(pkey, wpLibCtx, buf, bufLen, rsaSig, &rsaSigLen,
            padMode, md, mgf1Md);
    }
    if ((err == 0) && (padMode != RSA_PKCS1_PSS_PADDING)) {
        PRINT_MSG("Verify with OpenSSL");
        err = test_pkey_verify(pkey, osslLibCtx, buf, bufLen, rsaSig, rsaSigLen,
            padMode, md, mgf1Md);
    }

#ifdef WP_HAVE_SHA256
    /* OpenSSL doesn't allow RSA signatures with no padding. */
    if ((err == 0) && (padMode != RSA_NO_PADDING)) {
        PRINT_MSG("Test creating/verifying a signature");
        PRINT_MSG("Sign with OpenSSL");
        err = test_digest_sign(pkey, osslLibCtx, buf, bufLen, "SHA-256", rsaSig,
            &rsaSigLen, padMode);
    }
    if ((err == 0) && (padMode != RSA_NO_PADDING)) {
        PRINT_MSG("Verify with wolfprovider");
        err = test_digest_verify(pkey, wpLibCtx, buf, bufLen, "SHA-256", rsaSig,
            rsaSigLen, padMode);
    }
    if ((err == 0) && (padMode != RSA_NO_PADDING)) {
        PRINT_MSG("Verify bad signature with wolfprovider");
        rsaSig[1] ^= 0x80;
        res = test_digest_verify(pkey, wpLibCtx, buf, bufLen, "SHA-256", rsaSig,
            rsaSigLen, padMode);
        if (res != 1)
            err = 1;
    }
    if ((err == 0) && (padMode != RSA_NO_PADDING)) {
        PRINT_MSG("Sign with wolfprovider");
        rsaSigLen = RSA_size(rsaKey);
        err = test_digest_sign(pkey, wpLibCtx, buf, bufLen, "SHA-256", rsaSig,
            &rsaSigLen, padMode);
    }
    if ((err == 0) && (padMode != RSA_NO_PADDING)) {
        PRINT_MSG("Verify with OpenSSL");
        err = test_digest_verify(pkey, osslLibCtx, buf, bufLen, "SHA-256",
             rsaSig, rsaSigLen, padMode);
    }
#endif

    EVP_PKEY_free(pkey);

    if (rsaSig)
        OPENSSL_free(rsaSig);
    if (buf)
        OPENSSL_free(buf);

    return err;
}


