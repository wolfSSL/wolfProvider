static const char* cast_state_str(int s)
{
    switch (s) {
        case FIPS_CAST_STATE_INIT:       return "INIT";
        case FIPS_CAST_STATE_PROCESSING: return "PROCESSING";
        case FIPS_CAST_STATE_SUCCESS:    return "SUCCESS";
        case FIPS_CAST_STATE_FAILURE:    return "FAILURE";
        default:                         return "?";
    }
}

static int dh_z_cold(void)
{
    return wc_GetCastStatus_fips(FIPS_CAST_DH_PRIMITIVE_Z)
        != FIPS_CAST_STATE_SUCCESS;
}
static int ecc_z_cold(void)
{
    return wc_GetCastStatus_fips(FIPS_CAST_ECC_PRIMITIVE_Z)
        != FIPS_CAST_STATE_SUCCESS;
}

/* Load a PKCS#8 key via wolfProvider (routes through wp_*_decode). Caller frees. */
static EVP_PKEY* load_priv_pkcs8(const unsigned char* der, size_t len)
{
    EVP_PKEY* pkey = NULL;
    PKCS8_PRIV_KEY_INFO* p8;
    const unsigned char* p = der;

    p8 = d2i_PKCS8_PRIV_KEY_INFO(NULL, &p, (long)len);
    if (p8 == NULL) {
        return NULL;
    }
    pkey = EVP_PKCS82PKEY_ex(p8, wpLibCtx, NULL);
    PKCS8_PRIV_KEY_INFO_free(p8);
    return pkey;
}

/* Load an SPKI public key via wolfProvider. Caller frees. */
static EVP_PKEY* load_pub_spki(const unsigned char* der, size_t len)
{
    const unsigned char* p = der;

    return d2i_PUBKEY_ex(NULL, &p, (long)len, wpLibCtx, NULL);
}

/*
 * TEST 1 + 2 (cold CASTs): an RSA load must fire only the RSA CAST, never the
 * DH/ECC primitive-Z CASTs; the SubjectPublicKeyInfo load also guards the
 * d2i_X509_PUBKEY recursion that used to SIGSEGV.
 */
static int test_rsa_load_no_foreign_cast(void)
{
    int err = 0;
    EVP_PKEY* priv = NULL;
    EVP_PKEY* pub = NULL;

    TEST_INFO("Test 1/2: RSA load must not fire DH/ECC primitive-Z CAST");

    if (!dh_z_cold() || !ecc_z_cold()) {
        TEST_ERROR("  Precondition failed: DH/ECC primitive-Z CAST not cold "
            "(DH=%s ECC=%s) - process isolation broken",
            cast_state_str(wc_GetCastStatus_fips(FIPS_CAST_DH_PRIMITIVE_Z)),
            cast_state_str(wc_GetCastStatus_fips(FIPS_CAST_ECC_PRIMITIVE_Z)));
        return TEST_FAILURE;
    }
    TEST_INFO("  Start: DH primitive-Z=%s, ECC primitive-Z=%s (cold)",
        cast_state_str(wc_GetCastStatus_fips(FIPS_CAST_DH_PRIMITIVE_Z)),
        cast_state_str(wc_GetCastStatus_fips(FIPS_CAST_ECC_PRIMITIVE_Z)));

    priv = load_priv_pkcs8(rsa_pkcs8_der, sizeof(rsa_pkcs8_der));
    if (priv == NULL) {
        TEST_ERROR("  Failed to load RSA PKCS#8 private key");
        err = TEST_FAILURE;
    }

    if (err == 0) {
        pub = load_pub_spki(rsa_spki_der, sizeof(rsa_spki_der));
        if (pub == NULL) {
            TEST_ERROR("  Failed to load RSA SubjectPublicKeyInfo public key");
            err = TEST_FAILURE;
        }
    }

    if (err == 0) {
        TEST_INFO("  After RSA loads: RSA-sign CAST=%s, DH primitive-Z=%s, "
            "ECC primitive-Z=%s",
            cast_state_str(
                wc_GetCastStatus_fips(FIPS_CAST_RSA_SIGN_PKCS1v15)),
            cast_state_str(wc_GetCastStatus_fips(FIPS_CAST_DH_PRIMITIVE_Z)),
            cast_state_str(wc_GetCastStatus_fips(FIPS_CAST_ECC_PRIMITIVE_Z)));

        if (wc_GetCastStatus_fips(FIPS_CAST_RSA_SIGN_PKCS1v15)
                != FIPS_CAST_STATE_SUCCESS) {
            TEST_ERROR("  RSA-sign CAST did not fire on RSA load (sanity)");
            err = TEST_FAILURE;
        }
    }

    if (err == 0 && !dh_z_cold()) {
        TEST_ERROR("  REGRESSION: DH primitive-Z CAST fired while loading an "
            "RSA key");
        err = TEST_FAILURE;
    }
    if (err == 0 && !ecc_z_cold()) {
        TEST_ERROR("  REGRESSION: ECC primitive-Z CAST fired while loading an "
            "RSA key");
        err = TEST_FAILURE;
    }

    EVP_PKEY_free(priv);
    EVP_PKEY_free(pub);

    if (err == 0) {
        TEST_INFO("  PASSED");
    }
    return err;
}

